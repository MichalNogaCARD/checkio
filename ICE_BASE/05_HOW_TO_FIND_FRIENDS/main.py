from itertools import chain


def check_connection(network, first, second) -> bool:
    nodes = {tuple(connection.split('-')) for connection in network}
    friends = {first}

    while friends != set():
        pairs = {pair for pair in nodes if any(one in pair for one in friends)}
        friends = set(chain(*pairs)) - friends
        if second in friends:
            return True
        nodes -= pairs

    return False


